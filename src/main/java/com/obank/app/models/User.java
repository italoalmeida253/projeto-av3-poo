package com.obank.app.models;

public class User {
    private String name;
    private String email;
    private String password;
    private String dateOfBirth;

    public String getName() {
        return name;
    }
    public String getEmail() {
        return email;
    }
    public String getPassword() {
        return password;
    }
    public String getDateOfBirth() {
        return dateOfBirth;
    }
}
